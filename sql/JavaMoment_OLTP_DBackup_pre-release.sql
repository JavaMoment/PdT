  select
    t."ID_ESTUDIANTE",t."FECHA_CREACION",t."CANT_RECLAMOS",
    to_char(to_date(t.fecha_creacion, 'yyyymmdd'), 'mm') mes,
    to_char(to_date(t.fecha_creacion, 'yyyymmdd'), 'yyyy') anio
